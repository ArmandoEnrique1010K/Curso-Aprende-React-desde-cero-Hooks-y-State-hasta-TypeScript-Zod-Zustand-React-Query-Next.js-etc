// CONSTANTES

// Las variables con const no se puede reasignar su valor, el valor definido se queda ahi todo el tiempo
const cliente = "Juan";

// En el caso de intentar reasignar el valor de la constante, se mostrara un error en la consola "Assignment to constant variable"
cliente = 30;

const precio = 1000;
console.log(precio);

// Es posible frecuentar el uso de const en el codigo fuente de un proyecto en lugar de let.

