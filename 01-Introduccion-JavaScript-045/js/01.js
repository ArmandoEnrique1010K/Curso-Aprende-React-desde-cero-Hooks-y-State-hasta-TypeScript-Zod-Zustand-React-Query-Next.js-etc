// VARIABLES

// JavaScript permite crear variables de 3 formas: var, let y const

// Hoy en dia no se recomienda escribir variables con var, en su lugar se utiliza let.

// Las variables no pueden iniciar con numeros, pero si puede finalizar con numeros. No pueden incluir caracteres especiales en su nombre

// Las variables tienen que ser descriptivas

// Crea la variable cliente y luego se asigna un valor.
let cliente = "Juan";

// El tipo de dato de la variable se asigna por su valor y no por la variable.

// Otros lenguajes como Java o C#, permite asignar el tipo de dato. En el caso de JS no le defines ningun tipo, porque es un lenguaje dinamico
cliente = 30;

// Puedes inicializar una variable sin ningun valor y luego asignar su valor
let precio;
precio = 1000;

// Imprime un mensaje en la consola
console.log(precio);

// Por lo general el codigo se escribe en ingles, incluyendo nombres de variables

// Si inicializas una variable sin ningun valor, en la consola se imprimira undefined

// Si imprimes una variable no definida, mostrar un error "precio is not defined"
